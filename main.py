@app.route("/contact", methods=['GET', 'POST'])
def contact():
    MY_EMAIL = os.getenv("my_email")
    PASSWORD = os.getenv("password")
    ADDRESS = os.getenv("address")
    PORT = os.getenv("port")
    if request.method == "POST":
        data_form = request.form
        username = data_form["username"]
        email = data_form["email-form"]
        phone = data_form["phone-form"]
        message = data_form["message-form"]
        with smtplib.SMTP("smtp.gmail.com", port=PORT) as connection:
            connection.starttls()
            connection.login(user=MY_EMAIL, password=PASSWORD)
            connection.sendmail(from_addr=MY_EMAIL,
                                to_addrs=ADDRESS,
                                msg=f"Subject:You get a message from {username}\n\n"
                                    f"Email : {email}\nPhone number : {phone}\nMessage: {message}")
        return render_template("contact.html", msg_sent=True)
    return render_template("contact.html", msg_sent=False, logged_in=current_user.is_authenticated)


@app.route("/new-post", methods=["GET", "POST"])
@admin_only
def add_new_post():
    form = CreatePostForm()
    if form.validate_on_submit():
        new_post = BlogPost(
            title=form.title.data,
            subtitle=form.subtitle.data,
            body=form.body.data,
            img_url=form.img_url.data,
            author_id=current_user.id,
            date=date.today().strftime("%B %d, %Y")
        )
        db.session.add(new_post)
        db.session.commit()
        return redirect(url_for("get_all_posts"))
    return render_template("make-post.html", form=form, logged_in=current_user.is_authenticated, current_user=current_user)


@app.route("/edit-post/<int:post_id>", methods=['GET', 'POST'])
@admin_only
def edit_post(post_id):
    post = BlogPost.query.get(post_id)
    edit_form = CreatePostForm(
        title=post.title,
        subtitle=post.subtitle,
        img_url=post.img_url,
        body=post.body
    )
    if edit_form.validate_on_submit():
        post.title = edit_form.title.data
        post.subtitle = edit_form.subtitle.data
        post.img_url = edit_form.img_url.data
        post.body = edit_form.body.data
        db.session.commit()
        return redirect(url_for("show_post", post_id=post.id))

    return render_template("make-post.html", form=edit_form, logged_in=current_user.is_authenticated, current_user=current_user)


