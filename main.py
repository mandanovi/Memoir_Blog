def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if current_user.is_anonymous:
            return abort(403, description="User is anonymous. You are forbidden to do this act.")
        elif current_user.id != 1:
            return abort(403, description="User is not an admin. You are forbidden to do this act.")
        else:
            return f(*args, **kwargs)
    return decorated_function


