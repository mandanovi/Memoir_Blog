@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        pw_hash = generate_password_hash(password=form.password.data, method='pbkdf2:sha256', salt_length=8)
        user = User.query.filter_by(email=form.email.data).first()
        if user:
            flash("You've already signed up with that email, log in instead")
            return redirect(url_for("login"))
        new_user = User(email=form.email.data, password=pw_hash, name=form.name.data)
        db.session.add(new_user)
        db.session.commit()
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user_login = User.query.filter_by(email=form.email.data).first()
        if user_login:
            if check_password_hash(user_login.password, form.password.data):
                login_user(user_login)
                return redirect(url_for("get_all_posts"))
            else:
                flash("Password incorrect, please try again.")
                return redirect(url_for("login"))
        else:
            flash("That email does not exist, please try again.")
            return redirect(url_for("login"))
    return render_template("login.html", form=form, logged_in=current_user.is_authenticated)


