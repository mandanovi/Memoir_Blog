@app.route("/post/<int:post_id>", methods=['GET', 'POST'])
def show_post(post_id):
    form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    gravatar = Gravatar(app,
                        size=100,
                        rating='g',
                        default='retro',
                        force_default=False,
                        force_lower=False,
                        use_ssl=False,
                        base_url=None)
    if form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to log-in or register to comment.")
            return redirect(url_for('login'))
        new_comment = Comment(author_id=current_user.id, post_id=post_id, text=form.body.data)
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for('show_post', post_id=post_id))
    form.body.data = ""
    return render_template("post.html", form=form, post=requested_post, gravatar=gravatar, logged_in=current_user.is_authenticated)


